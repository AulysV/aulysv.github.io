    <p>
      Je suis sûr que cette période de l'année était particulière : le soleil
      était fort mais pas insoutenable, les nuits sont fraiches, les vignes ne
      sont pas encore en fruits. Ça peut être amusant de se balader à travers
      les vignobles pleins de raisins. <br /> Finalement ce qui m'a le plus plu dans
      cette balade, ce sont les rencontres, ce sont elles qui font que même si je
      refaisais la même balade, l'expérience ne serait pas la même.
